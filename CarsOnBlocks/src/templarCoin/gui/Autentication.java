    private void tpMainStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_tpMainStateChanged
        if (tpMain.getSelectedComponent() == spList) {
            DefaultListModel model = new DefaultListModel();
            model.addAll(User.getUserList());
            list.setModel(model);
        }
    }//GEN-LAST:event_tpMainStateChanged

    private void listValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listValueChanged
        if (list.getSelectedIndex() >= 0) {
            User u = (User) list.getSelectedValues()[0];
            txtUser.setText(u.getName());
            txtPassword.requestFocus();
            tpMain.setSelectedComponent(pnLogin);
        }
    }//GEN-LAST:event_listValueChanged

    private void btLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoginActionPerformed
        try {
            User user = User.load(txtUser.getText(), new String(txtPassword.getPassword()));
            InterfaceRemoteMiner miner= ( InterfaceRemoteMiner)
                    RMI.getRemote(txtRemote.getText());
            user.setMiner(miner);
            this.dispose();
            new TemplarCoinGUI(user).setVisible(true);
//            new SecureFiles(user).setVisible(true);
        } catch (Exception ex) {
            Logger.getLogger(Autentication.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btLoginActionPerformed

